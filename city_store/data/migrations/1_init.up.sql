CREATE TABLE IF NOT EXISTS Bikes (
  ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
  TYPE ENUM('MOUNTAIN', 'CITY', 'ROAD') NOT NULL,
  AVAILABLE INT NOT NULL DEFAULT 0,
  CITY ENUM('KRAGUJEVAC', 'NOVISAD', 'SUBOTICA') NOT NULL
);

CREATE TABLE IF NOT EXISTS Rentals (
  ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
  RENTAL_CITIZEN_ID VARCHAR(13) NOT NULL,
  RENTAL_DATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  RENTAL_BIKE_ID VARCHAR(20) NOT NULL,
  CITY ENUM('KRAGUJEVAC', 'NOVISAD', 'SUBOTICA') NOT NULL,
  RETURNED_DATE DATETIME DEFAULT NULL,
  COMPLETED BOOLEAN NOT NULL DEFAULT FALSE
);
